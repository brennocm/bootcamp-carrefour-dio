//declarativas
function funcao(){
    console.log('sou uma mensagem de uma funcao declarativa');
}
funcao();

//expressão de função
//com nomeação
var functionFuncao = function (){
    console.log("Sou uma mensagem de função de expressão");
}
functionFuncao();

//arrow function
var arrowFuncao = () =>{
    console.log('Sou uma Arrow Function');
}
arrowFuncao();